number = 1000

print("The number is {:.3f}".format(number)) #3 liczby po przecinku i zaokrągla liczbę
print("The number is {:,}".format(number)) #przecinki jak są 3 zera
print("The number is {:b}".format(number)) #binarne liczby
print("The number is {:o}".format(number)) #
print("The number is {:X}".format(number)) #hexadecymalne liczby
print("The number is {:E}".format(number)) #notacja wykładnicza


